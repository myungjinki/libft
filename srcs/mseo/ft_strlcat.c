#include "libft.h"

size_t	ft_strlcat(char *dest, const char *src, size_t detsize)
{
	size_t	i;
	size_t	dest_len;
	size_t	src_len;

	dest_len = ft_strlen(dest);
	src_len = ft_strlen(src);
	i = 0;
	if (detsize < dest_len + 1)
		return (detsize + src_len);
	if (detsize > dest_len + 1)
	{
		while (src[i] != '\0' && dest_len + 1 + i < detsize)
		{
			dest[dest_len + i] = src[i];
			i++;
		}
	}
	dest[dest_len + i] = '\0';
	return (dest_len + src_len);
}
