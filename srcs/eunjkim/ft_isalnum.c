#include "libft.h"

int		ft_isalnum(int c)
{
	int answer;

	answer = 0;
	if (c >= 48 && c <= 57)
		answer = 1;
	if ((c >= 65 && c <= 90) || (c >= 97 && c <= 122))
		answer = 1;
	if (answer)
		return (answer);
	return (0);
}
