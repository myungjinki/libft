#include "libft.h"

int		ft_lstsize(t_list *lst)
{
	t_list		*n;
	int			i;

	if (!(lst))
		return (0);
	i = 1;
	n = lst->next;
	while (n)
	{
		i++;
		n = n->next;
	}
	return (i);
}
