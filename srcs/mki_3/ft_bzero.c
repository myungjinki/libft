#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*tmp;

	tmp = s;
	if (n == 0)
		return ;
	i = 0;
	while (i < n)
		tmp[i++] = 0;
}
